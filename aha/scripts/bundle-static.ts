/**
 * bundle-static.ts
 *
 * GoTH 向けに JS アセットをバンドル・出力する。
 *
 * 出力先: goth/static/js/
 *   - htmx.min.js    — htmx.org をバンドル
 *   - alpine.min.js   — Alpine.js をバンドル
 *
 * CSS は別途 @tailwindcss/cli で生成（package.json の gen:css 参照）
 *
 * Usage: bun run scripts/bundle-static.ts
 */

import * as path from "path";
import * as fs from "fs";

const gothStatic = path.resolve("../goth/static");
const jsDir = path.join(gothStatic, "js");

fs.mkdirSync(jsDir, { recursive: true });

// ─── 1. htmx.org — 公式 min ビルドをコピー ───────────
// Bun.build でバンドルするとグローバル変数 htmx がリネームされるため、
// 公式の htmx.min.js をそのままコピーする。

console.log("Copying htmx.org (official min build)...");

const htmxMin = path.resolve("node_modules/htmx.org/dist/htmx.min.js");
fs.copyFileSync(htmxMin, path.join(jsDir, "htmx.min.js"));
console.log(`  → ${path.relative(".", path.join(jsDir, "htmx.min.js"))}`);

// ─── 2. Alpine.js — CDN ビルドをコピー ─────────────
// Bun.build で ESM バンドルすると CDN ビルドと互換性のない出力になるため、
// alpinejs の公式 CDN ビルド (cdn.min.js) をそのままコピーする。
// CDN ビルドは defer 読み込みで自動的に Alpine.start() を呼ぶ。

console.log("Copying Alpine.js (CDN build)...");

const alpineCdn = path.resolve("node_modules/alpinejs/dist/cdn.min.js");
fs.copyFileSync(alpineCdn, path.join(jsDir, "alpine.min.js"));
console.log(`  → ${path.relative(".", path.join(jsDir, "alpine.min.js"))}`);

// ─── 3. react-barcode カスタムエレメント バンドル ──────

console.log("Bundling react-barcode custom element...");

const barcodeResult = await Bun.build({
  entrypoints: [path.resolve("src/components/ReactBarcodeWrapper.tsx")],
  outdir: jsDir,
  naming: "react-barcode.min.js",
  minify: true,
  target: "browser",
});

if (!barcodeResult.success) {
  console.error("react-barcode bundle failed:", barcodeResult.logs);
  process.exit(1);
}
console.log(`  → ${path.relative(".", path.join(jsDir, "react-barcode.min.js"))}`);

// ─── 4. icons_gen.templ — @iconify-json/heroicons から SVG 抽出 ──

console.log("Generating icons_gen.templ from icon_manifest.json...");

const manifestPath = path.resolve("../goth/icon_manifest.json");
if (fs.existsSync(manifestPath)) {
  const iconNames: string[] = JSON.parse(fs.readFileSync(manifestPath, "utf-8"));
  const heroicons = JSON.parse(
    fs.readFileSync(path.resolve("node_modules/@iconify-json/heroicons/icons.json"), "utf-8"),
  );
  const width = heroicons.width || 24;
  const height = heroicons.height || 24;

  // アイコン名 → PascalCase templ 関数名 (e.g. "pencil-square" → "IconPencilSquare")
  function iconFuncName(name: string): string {
    return "Icon" + name.split("-").map((w: string) => w.charAt(0).toUpperCase() + w.slice(1)).join("");
  }

  const lines: string[] = [];
  lines.push("// Code generated by bundle-static.ts. DO NOT EDIT.");
  lines.push("package ui");
  lines.push("");

  // 汎用ディスパッチ関数: Icon(name, class) → switch で各アイコンを呼び出し
  lines.push("// Icon はアイコン名とCSSクラスを受け取りSVGを出力する");
  lines.push(`templ Icon(name string, cls string) {`);
  lines.push(`\tswitch name {`);
  for (const name of iconNames) {
    const data = heroicons.icons[name];
    if (!data) {
      console.warn(`  ⚠ icon "${name}" not found in heroicons`);
      continue;
    }
    // SVG の fill/stroke 属性を判定
    const isFilled = data.body.includes('fill-rule') || (!data.body.includes('stroke=') && !data.body.includes('fill="none"'));
    const fillAttr = isFilled ? `fill="currentColor"` : `fill="none" stroke="currentColor" stroke-width="1.5"`;
    lines.push(`\t\tcase "${name}":`);
    lines.push(`\t\t\t<svg xmlns="http://www.w3.org/2000/svg" viewBox="0 0 ${width} ${height}" ${fillAttr} class={ cls }>${data.body}</svg>`);
  }
  lines.push(`\t}`);
  lines.push(`}`);
  lines.push("");

  const iconsTemplPath = path.resolve("../goth/internal/ui/icons_gen.templ");
  fs.writeFileSync(iconsTemplPath, lines.join("\n"));
  console.log(`  → ${path.relative(".", iconsTemplPath)} (${iconNames.length} icons)`);
} else {
  console.log("  ⚠ icon_manifest.json not found, skipping icons_gen.templ");
}

// ─── 完了 ─────────────────────────────────

console.log("\nDone! Assets generated for ../goth/");
console.log("  static/js/htmx.min.js");
console.log("  static/js/alpine.min.js");
console.log("  static/js/react-barcode.min.js");
console.log("  internal/ui/icons_gen.templ");
